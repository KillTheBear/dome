package deamon;

public class AddressList {
    /**
     * name:同学姓名
     * address:同学地址
     * tel: 同学电话
     */
    private String name;
    private String address;
    private String tel;

    public AddressList(String name, String address, String tel) {
        this.name = name;
        this.address = address;
        this.tel = tel;
    }
}
